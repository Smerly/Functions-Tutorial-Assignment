def area(width, height):
    result = width * height
    return result



result = area(5, 6)
print(result)

result = area(100, 3)
print(result)

result = area(12, 12)
print(result)

def subtract(num1, num2):
    result = num1 - num2
    return result


result = subtract(100, 30)
print(result)



def divide(dividend, divisor):
    quotient = dividend / divisor
    return quotient

quotient = divide(6, 3)
print(quotient)


